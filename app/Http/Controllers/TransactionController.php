    {
        $items_arr = $request->items;
        if(!$items_arr)
        {
            return back()->withError('Please supply at least 1 item');
        }else{
            $items = Item::whereIn('id', $items_arr)->get();
            return view('pages.transactions.create-transaction', compact('items'));
        }
    }

